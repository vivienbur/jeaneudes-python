# For loop
for i in range(5):
    print(i)


for num in range(1, 6):
    print(num)



# Example 5: Loop with step size
for num in range(3, 13, 2):
    print(num)


# While loop
x = 0
while x < 5:
    print(x)
    x += 1


# Looping through a list
my_list = [1, 2, 3, 4, 5]
for item in my_list:
    print(item)